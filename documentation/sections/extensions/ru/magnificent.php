<h3>Magnificent - зум изображений</h3>
<h6>HTML Website Templates используют расширение
  <b>Magnificent.js</b> для реализации зума изображений</h6>

<p>Базовая HTML разметка выглядит следующим образом:</p>

<pre>
  <code>
    &lt;!-- Magnificent--&gt;
    &lt;div mag-thumb="inner"&gt;
      &lt;img src="images/puppyafter.jpg" width="500" height="300"/&gt;
    &lt;/div&gt;
    &lt;div mag-zoom="inner"&gt;
      &lt;img src="images/puppyafter.jpg"/&gt;
    &lt;/div&gt;
  </code>
</pre>

