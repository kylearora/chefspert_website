<h3>RD Material Tabs. Табы</h3>
<p>HTML Website Templates используют расширение <b>RD Material Tabs</b> для реализации табов. Для внедрения табов на целевую страницу необходимо воспользоваться  следующей разметкой.</p>

<pre>
  <code>
        &lt;!-- RD-material-tabs --&gt;
        &lt;section class="rd-material-tabs"&gt;
          &lt;div class="rd-material-tabs__list"&gt;
            &lt;!-- List of tab headings --&gt;
            &lt;ul&gt;
              &lt;li&gt;
                &lt;a href="#"&gt;Tab 1&lt;/a&gt;
              &lt;/li&gt;
              &lt;li&gt;
                &lt;a href="#"&gt;Tab 2&lt;/a&gt;
              &lt;/li&gt;
              &lt;li&gt;
                &lt;a href="#"&gt;Tab 3&lt;/a&gt;
              &lt;/li&gt;
            &lt;/ul&gt;
          &lt;/div&gt;
          &lt;!-- Container for your content --&gt;
          &lt;div class="rd-material-tabs__container"&gt;
            &lt;!-- Content for first tab --&gt;
            &lt;div&gt;...&lt;/div&gt;
            &lt;!-- Content for second tab --&gt;
            &lt;div&gt;...&lt;/div&gt;
            &lt;!-- Content for third tab --&gt;
            &lt;div&gt;...&lt;/div&gt;
          &lt;/div&gt;
        &lt;/section&gt;
</code>
</pre>