<h3>Responsive Tabs. Табы</h3>
<p>HTML Website Templates используют расширение <b>jQuery Responsive Tab</b> для реализации табов. Для внедрения табов на целевую страницу необходимо воспользоваться  следующей разметкой.</p>

<pre><code>
        &lt;div class="responsive-tabs"&gt;
            &lt;ul class="resp-tabs-list"&gt;
                &lt;li&gt; Tab 1 Title &lt;/li&gt;
                &lt;li&gt; Tab 2 Title &lt;/li&gt;
                &lt;li&gt; Tab 3 Title &lt;/li&gt;
            &lt;/ul&gt;

            &lt;div class="resp-tabs-container"&gt;
                &lt;div&gt; Tab 1 Body &lt;/div&gt;
                &lt;div&gt; Tab 2 Body &lt;/div&gt;
                &lt;div&gt; Tab 3 Body &lt;/div&gt;
            &lt;/div&gt;
        &lt;/div&gt;
    </code></pre>