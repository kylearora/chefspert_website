<h3>Magnificent. Image zoom </h3>
<h6>HTML Website Templates use <b>Magnificent.js</b> extension to implement image zoom.
</h6>

<p>The basic HTML marking looks the following way:</p>

<pre>
  <code>
    &lt;!-- Magnificent--&gt;
    &lt;div mag-thumb="inner"&gt;
      &lt;img src="images/puppyafter.jpg" width="500" height="300"/&gt;
    &lt;/div&gt;
    &lt;div mag-zoom="inner"&gt;
      &lt;img src="images/puppyafter.jpg"/&gt;
    &lt;/div&gt;
  </code>
</pre>

