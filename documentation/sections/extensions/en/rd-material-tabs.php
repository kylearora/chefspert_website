<h3>RD Material Tabs</h3>
<h6>HTML Website Template uses RD Material Tabs to implement tabs. To add a tab, use the following marking:</h6>
<pre><code>
    // RD Material Tabs
    &lt;div class="rd-material-tabs"&gt;
      &lt;div class="rd-material-tabs__list"&gt;
        &lt;ul&gt;
          &lt;li&gt;
            &lt;a href='#'&gt;  Tab 1 &lt;/a&gt;
          &lt;/li&gt;
          &lt;li&gt;
            &lt;a href='#'&gt;  Tab 2 &lt;/a&gt;
          &lt;/li&gt;
          &lt;li&gt;
            &lt;a href='#'&gt;  Tab 3 &lt;/a&gt;
          &lt;/li&gt;
        &lt;/ul&gt;
      &lt;/div&gt;
      &lt;div class="rd-material-tabs__container"&gt;
        &lt;div&gt;
          Content 1
        &lt;/div&gt;
        &lt;div&gt;
          Content 2
        &lt;/div&gt;
        &lt;div&gt;
          Content 3
        &lt;/div&gt;
      &lt;/div&gt;
    &lt;/div&gt;
  </code></pre>