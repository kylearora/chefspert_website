<h6>HTML Website Template Template use <b>TimeCircles.js</b> extension to implement functionality for timer with radial progress bar.</h6>

<h4>Inserting timer to page</h4>

<p>In order to implement countdown timer to target HTML page you should use the following code structure:</p>

<code>&lt;div id="DateCountdown" data-date="2016-10-01 00:00:00" class="text-white"&gt;&lt;/div&gt;</code>

<p>Data-date attribute defines counting start or end date in the following format: <b>data-date="yyyy-mm-dd
        hh:mm:ss"</b></p>

<p>For example,</p>
<code>&lt;div id="DateCountdown" data-date="2016-10-01 00:00:00" class="text-white"&gt;&lt;/div&gt;</code>

<p>Countdown will take place till <b>00:00 October 01, 2016</b>.</p>