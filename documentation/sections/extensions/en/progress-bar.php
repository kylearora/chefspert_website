<h3>Progress Bar. Horizontal progress bars</h3>
<h6>HTML Website Template uses <b>Bootstrap Progress Bars</b> extension to implement horizontal progress bars.</h6>
<p>The basic HTML marking for creating a progress bar should look the following way:</p>
<pre><code>
    &lt;div class="progress"&gt;
      &lt;div class="progress-bar"
           role="progressbar"
           aria-valuenow="60"
           aria-valuemin="0"
           aria-valuemax="100"&gt;
        &lt;span class="sr-only"&gt;60% Complete&lt;/span&gt;
      &lt;/div&gt;
    &lt;/div&gt;
  </code></pre>
<p> where:</p>
<ul class="marked-list">
  <li>
    <dl class="inline-term">
      <dt>aria-valuenow</dt>
      <dd>the current value of the progress bar;</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>aria-valuemin</dt>
      <dd>the minimal value of the progress bar;</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>aria-valuemax</dt>
      <dd>the maximal value of the progress bar.</dd>
    </dl>
  </li>
</ul>

<p>You can use any additional  <b>Bootstrap</b>, связанные с <b>progress bars</b> settings, related to progress bars that are presented on the
  <b><a href="http://getbootstrap.com/components/#progress">Bootstrap</a> </b> official website.</p>





