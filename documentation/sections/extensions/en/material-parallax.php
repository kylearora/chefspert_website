<h3>Material parallax</h3>
<p>To replace the background image on parallax sections, you need to enter image path in the <b>data-parallax-img</b> attribute.</p>
<pre><code>
&lt;section class="section parallax-container" data-parallax-img="images/trunk-bg-image.jpg"&gt;
    &lt;div class="parallax-content"&gt;
      &lt;div class="shell"&gt;
        ...
      &lt;/div&gt;
    &lt;/div&gt;
  &lt;/section&gt;
</code></pre>