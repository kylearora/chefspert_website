<h3>Circle-progress. Radial progress bar with gradient</h3>
<h6>HTML Website Template uses <b>jquery-circle-progress.js</b> to implement radial progress bars with gradient.</h6>
<p>The basic HTML marking for creating a progress bar looks the following way:</p>
<pre><code>
    &lt;!-- Circle Progress Bar--&gt;
    &lt;div data-value="0.8"
         data-gradient="#5aba67, #78c157"
         data-empty-fill="rgb(245,245,245)"
         data-size="150" class="progress-bar-circle"&gt;
      &lt;span&gt;&lt;/span&gt;
    &lt;/div&gt;
  </code></pre>
<p> where:</p>
<ul class="marked-list">
  <li>
    <dl class="inline-term">
      <dt>data-value</dt>
      <dd>progress bar value (required attribute);</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>data-gradient</dt>
      <dd>progress bar filled part color value (required attribute);</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>data-size</dt>
      <dd>progress bar size in pixels;</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>data-empty-fill</dt>
      <dd>progress bar empty part color value.</dd>
    </dl>
  </li>
</ul>











