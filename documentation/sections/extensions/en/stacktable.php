<h3>Stacktable. Adaptive tables
</h3>
<p>HTML Website Template uses <b>Stacktable</b> extension for implementation of adaptive tables.To make a target table adaptive, just add the <b>data-responsive="true"</b> attribute to its marking:
</p>
<pre><code>
        &lt;table data-responsive="true"  &gt;
            ...
        &lt;/table&gt;
    </code></pre>