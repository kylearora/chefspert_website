<h6>HTML Website Template use <b>ResponsiveTabs.js</b> extension to implement tabs functionality. In order to add tabs to target pages you should use the following code structure.</h6>

<pre><code>&lt;div class="responsive-tabs"&gt;
    &lt;ul class="resp-tabs-list"&gt;
        &lt;li&gt; Tab 1 Title &lt;/li&gt;
        &lt;li&gt; Tab 2 Title &lt;/li&gt;
        &lt;li&gt; Tab 3 Title &lt;/li&gt;
    &lt;/ul&gt;

    &lt;div class="resp-tabs-container"&gt;
        &lt;div&gt; Tab 1 Body &lt;/div&gt;
        &lt;div&gt; Tab 2 Body &lt;/div&gt;
        &lt;div&gt; Tab 3 Body &lt;/div&gt;
    &lt;/div&gt;
&lt;/div&gt;
</code></pre>