<h3>Right to Left (RTL)</h3>

<p>В Website Templates предусмотрен вариант реализации rtl варианта сайта.</p>
<p>Пример rtl страницы вы можете найти по ссылке index-rtl.html Вашего шаблона</p>

<p>Для имплементации rtl необходимо на <mark>html</mark> добавить атрибут <mark>dir="rtl"</mark> и вместо обычных стилей шаблона в <mark>&lt;head&gt;</mark></p>

<pre><code>&lt;link rel="stylesheet" href="css/bootstrap.css"&gt;
&lt;link rel="stylesheet" href="css/style.css"&gt;
</code></pre>

<p>подключить</p>

<pre><code>&lt;link rel="stylesheet" href="css/bootstrap-rtl.css"&gt;
&lt;link rel="stylesheet" href="css/style-rtl.css"&gt;
</code></pre>

<p>Для конвертации css который вы будете дописывать в rtl версию вы можете использовать - <a href="http://rtlcss.com/playground/">rtlcss.com</a></p>