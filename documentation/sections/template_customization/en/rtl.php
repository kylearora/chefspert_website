<h3>Right to Left (RTL)</h3>

<p>The realization of an RTL-variant of the website is foreseen in Website Templates.</p>
<p>You can find the example of an RTL-page by following index-rtl.html in your template.</p>

<p>To implement RTL, add <mark>dir="rtl"</mark> attribute in <mark>html</mark>, and instead of regular template styles in <mark>&lt;head&gt;</mark></p>

<pre><code>&lt;link rel="stylesheet" href="css/bootstrap.css"&gt;
&lt;link rel="stylesheet" href="css/style.css"&gt;
</code></pre>

<p>enable</p>

<pre><code>&lt;link rel="stylesheet" href="css/bootstrap-rtl.css"&gt;
&lt;link rel="stylesheet" href="css/style-rtl.css"&gt;
</code></pre>

<p>To convert CSS, which you are going to add in the RTL version, you can use <a href="http://rtlcss.com/playground/">rtlcss.com</a></p>