<h3>Changing the Logo</h3>
<h6>In HTML Website Template, you can use either custom image or text logo.</h6>

<p class="alert alert-info">Attention: HTML Website Template is an HTML product. Thus, the logo should be changed manually in every target .html file.</p>

<h4>Changing the Logo</h4>

<p>Header logo for the desktop version of the site should be changed in Page Head section of the target page with the <b>.rd-navbar-brand</b> class.</p>

<pre><code>
        &lt;!--Navbar Brand--&gt;
        &lt;div class="rd-navbar-brand"&gt;
            &lt;a href="index.html"&gt;
              &lt;img src='path/to/your-logo.png' alt=''/&gt;
            &lt;/a&gt;
        &lt;/div&gt;
    </code></pre>

<h4>How to change footer logo</h4>
<p>Footer logo is changed in <b>Page Footer</b> section of the block with <b>.footer-brand</b> or <b>.brand</b>class.</p>

<pre>
  <code>
        &lt; !--Footer Brand--&gt;
        &lt; div class="footer-brand"&gt;
            &lt; a href="index.html"&gt;
                &lt; img src='path/to/your-footer-logo.png' alt=''/&gt;
            &lt;/a&gt;
        &lt;/div&gt;
  </code>
</pre>
