<h3>Setting up a Shop</h3>
<p>HTML Website Template is an HTML template that targets developers. A Shop example in HTML Website Template is used just for simplifying the successive work on the
  product that developers carry out as a part of their orders. It does not contain functionality of implementing a live store.</p>

<p class="alert alert-warning">Attention: HTML Website Template does not have the functionality for creating a working online-shop.
  The Demo just has some examples of HTML marking for implementing a store based on HTML Website Template.</p>


