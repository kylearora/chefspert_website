<h3>Search by pages setup</h3>

<p>HTML Website Template comes with <b>RD Search</b> plugin for implementing search by pages.</p>

<h4>Changing the date on the page</h4>
<p>To change the date on the target page, find the meta-tag with <b>name="date"</b> attribute and change the value of the <b>'content'</b> attribute.</p>
<pre><code>
        &lt;meta name="date" content="..."&gt;
    </code></pre>
<h4>How to change tags on the page</h4>
<p>To change tags on the target page, find the <b>meta-tag</b> with <b>name="keywords"</b> attribute and change the value of the <b>'content'</b> attribute.</p>
<pre><code>
        &lt;meta name="keywords" content="HTML Website Template"&gt;
    </code></pre>
<p>hese tags are required for the correct functioning of the search. If you add new pages, you should also add these meta-tags.</p>