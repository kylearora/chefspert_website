<h3>Template scroll animation setup</h3>
<p>    HTML Website Template uses <b>Wow.js</b> extension and css library <b>Animate.css</b> for
  implementing the scroll animation. To enable animation on page scroll, add <b>.wow-animation</b> class for the <b>html</b> tag.</p>
<pre><code>&lt;html lang="en" class="wow-animation"&gt;</code></pre>

<p>You should also specify the <b>.wow</b> class  and the corresponding class for the target element. </p>
<pre><code>
        &lt;div class=wow fadeIn"&gt;
        ...
        &lt;/div&gt;
    </code></pre>

<p>You can also set the animation delay, using the <b>data-wow-delay</b> attribute.</p>
<pre><code>
        &lt;div data-wow-delay="0.2s" class="wow fadeIn"&gt;
        ...
        &lt;/div&gt;
    </code></pre>

<p>You can find the list of available animations on the <b>Components > Toolkit Components > Animations</b> template page.
  You can find a list of classes for generating different animated effects below.</p>

<ul class="marked-list">
    <li>fadeIn</li>
    <li>fadeLeft</li>
    <li>fadeRight</li>
    <li>fadeUp</li>
    <li>fadeDown</li>
    <li>slideLeft</li>
    <li>slideRight</li>
    <li>slideUp</li>
    <li>slideDown</li>
</ul>
