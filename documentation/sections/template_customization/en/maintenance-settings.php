<h3>Maintenance Page Setup</h3>
<p>HTML Website Template contains some ready-made examples of Maintenance and Coming Soon pages in Pages section. You can find some information about setting up these pages below .</p>
<h4>Setting up the countdown date</h4>
<p>HTML Website Template uses <b>jQuery Countdown</b> plugin for implementing the countdown functionality. You can find more detailed information about
  working with the plugin in <b><a href="index.php?lang=en&section=extensions#countdown">Extensions > Countdown</a></b> section .</p>
<p>To set up the countdown timer in HTML Website Template, please specify the target date in <b>data-time</b> attribute in <b>"DD Mon YYYY"</b> format in the corresponding countdown marking section.</p>

<pre><code><!-- Countdown-->
        &lt;div class="countdown-custom countdown-ellipse"&gt;
        &lt;div data-type="until" data-time="25 Apr 2017" data-format="dhms" class="countdown"&gt;&lt;/div&gt;
        &lt;/div&gt;
    </code></pre>
