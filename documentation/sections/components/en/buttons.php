<h3>Buttons</h3>
<h6>In HTML Website Template, the buttons are defined with the help of 3  classes:</h6>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>.btn</dt>
            <dd>the basic button class;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>.btn-xs, .btn-sm, .btn-md, .btn-lg, .btn-xl</dt>
            <dd>button layout classes, responsible for its size;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>.btn-default, .btn-primary, .btn-secondary-*</dt>
            <dd>button styling classes, responsible for its color and background.</dd>
        </dl>
    </li>
</ul>




<p>Here is an example:</p>
<pre><code>
        &lt;a href="#" class="btn btn-lg btn-primary"&gt;Read More&lt;/a&gt;
    </code></pre>
<p>In this example, one creates a large button with primary template color styling.</p>