<h3>Кнопки</h3>
<h6>В HTML Website Templates кнопки определяются с помощью 3 составных классов:</h6>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>.btn</dt>
            <dd>базовый класс кнопки;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>.btn-xs, .btn-sm, .btn-md, .btn-lg, .btn-xl</dt>
            <dd>классы лейаута кнопки, отвечающие за ее размер;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>.btn-default, .btn-primary</dt>
            <dd>классы стилизации кнопки, отвечающие за ее цвет и фон.</dd>
        </dl>
    </li>
</ul>
<p>Рассмотрим пример,</p>
<pre><code>
        &lt;a href="#" class="btn btn-lg btn-primary"&gt;Read More&lt;/a&gt;
    </code></pre>
<p>В данном примере создается кнопка большого размера со стилизацией по первичному цвету шаблона.</p>