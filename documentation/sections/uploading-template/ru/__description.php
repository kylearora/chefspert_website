<h2 class="item5">Загрузка шаблона </h2>

<p>Чтобы сделать сайт "живым", загрузите весь контент папки "<strong>site</strong>" с компьютера на хостинг-сервер.</p>

<p>Внимание: корневая папка веб-сайта зависит от структуры директорий на хостинг-сервере. Если вы просто загрузите папку
    &quot;<strong>site</strong>&quot;, адрес вашего веб-сайта будет выглядеть как
    <strong>http://your_domain_name/site</strong>. Чтобы сайт выглядел как <strong>http://your_domain_name/</strong>,
    просто откройте папку &quot;<strong>site</strong>&quot; и загрузите ее контент. </p>

<p class="alert alert-info">Данный <a href="/help/how-upload-files-server-2.html" target="_blank">видео туториал
        покажет, как загружать файлы</a>
    на сервер при помощи FTP-клиента FileZilla.</p>

<p class="alert alert-info">Данный
    <a href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html" target="_blank">видео
        туториал покажет, как загружать файлы</a> на сервер при помощи панели управления хостингом (WebHost Manager,
    WHM).</p>

<p class="alert alert-danger">Ни в коем случае не загружайте папку sources или другие исходные файлы шаблона на
    хостинг-сервер. </p>
